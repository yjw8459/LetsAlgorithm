package yjw.datastructure;

public class Array {

    public static void main(String[] args) {

        String x = "Hello! ";
        String y = "How are you ";
        String z = "today?";
        System.out.println(x + y + z);

        String[] array = {"Hello! ", "How are you ", "today?"};
        System.out.println(array[0] + array[1] + array[2]);
        
    }

}
